package day05operators;

public class Decrement01 {

	public static void main(String[] args) {
		
		// int data type de bir variable olusturun 
		// bu variablenin degerinin 3 yontemi de kullanarak her defasinda 1 azaltin.
		
		int num1 = 12;
		
		// uzun yol
		num1 = num1 - 1;
		System.out.println(num1);
		
		// kisa yol
		
		num1 -= 1;
		System.out.println(num1);
		
		// en kisa yol
		num1--;
		System.out.println(num1);

	}

}
