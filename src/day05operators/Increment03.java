package day05operators;

public class Increment03 {

	public static void main(String[] args) {
	
		// int data type de iki tane variable olusturun.
		// bu variable leri en kisa yolu kullanarak 1 artirin.
		
		// En kisa yol sadece 1 artirmak icin kullanilir.
		
		int sayi1 =12;  // variable isminden sonra iki kere + sembolu yazarsaniz
		sayi1++;        // o variable nin degeri 1 artar.
		
		System.out.println(sayi1);
		
		int sayi2 = 20;
		sayi2++;
		System.out.println(sayi2);

		
	}

}
