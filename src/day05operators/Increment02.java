package day05operators;

public class Increment02 {

	public static void main(String[] args) {
		
		// int data type' de iki variable olusturun birinciyi
		// birinciyi 3, ikinciyi 7 arttirin.
		
		int sayi1 =12;
		// sayi1 = sayi1 + 3
		
		sayi1 += 3;   // increment ==> kisa yol
		System.out.println(sayi1);  //==> + ile = arasinda bosluk yok
		
		int sayi2 = 20;
		sayi2 += 7;
		System.out.println(sayi2);

	}

}
