package day05operators;

public class Increment01 {
	
	public static void main(String[] args) {
		// int data type' de iki variable olusturun birinciyi
		// birinciyi 3, ikinciyi 7 arttirin.
		
		int sayi1 = 12;
		int sayi2 = 20;
		
		sayi1 = sayi1 + 3;
		System.out.println(sayi1);  //15
		
		sayi2 = sayi2 + 7;
		System.out.println(sayi2);  //27

	}

}
