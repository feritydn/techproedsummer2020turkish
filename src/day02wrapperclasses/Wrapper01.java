package day02wrapperclasses;

public class Wrapper01 {    // wrapper class primitive type a method eklenmesidir.

	public static void main(String[] args) {
		// int data type nin maksimun ve minimum degerlerini bulup ekrana yazdiriniz.
		// int in wrapper classinin kullanacagiz yani Integer
		
		System.out.println(Integer.MAX_VALUE); // bir classin icindeki methodlara ulasmak icin
		System.out.println(Integer.MIN_VALUE); // class isminden sonra nokta koy, listeden secim yap
		
		// long data type nin maksimun ve minimum degerlerini bulup ekrana yazdiriniz.
        //Wrapper = cevreleyen, kusatan. bayram sekerinin kagidi gibi
		
		System.out.println(Long.MAX_VALUE);
		System.out.println(Long.MIN_VALUE);
		
		// ODEV; tum primitivlerin max ve min degerlerini Wapper classlarini kullanarak ekrana yaz
		
		
		
	}

}
